package org.WdtcDownload.SetFilePath;

public class GamePath {
    public String starter_here;
    public String game_path;
    public String v_lib_path;
    public String game_lib_path;
    public String Game_assetsDir;

    public String getGame_lib_path() {
        return game_lib_path;
    }

    public void setGame_lib_path(String game_lib_path) {
        this.game_lib_path = game_lib_path;
    }

    public String getGame_assetsDir() {
        return Game_assetsDir;
    }

    public void setGame_assetsDir(String game_assetsDir) {
        Game_assetsDir = game_assetsDir;
    }

    public String getStarter_here() {
        return starter_here;
    }

    public void setStarter_here(String starter_here) {
        this.starter_here = starter_here;
    }

    public String getGame_path() {
        return game_path;
    }

    public void setGame_path(String game_path) {
        this.game_path = game_path;
    }


    public String getV_lib_path() {
        return v_lib_path;
    }

    public void setV_lib_path(String v_lib_path) {
        this.v_lib_path = v_lib_path;
    }
}
